## 6.2 Reserva de capitalización

**Fuente oficial**

- [Novedades AEAT 2025 - reserva de capitalización](https://sede.agenciatributaria.gob.es/Sede/impuesto-sobre-sociedades/novedades-impuesto-sobre-sociedades/novedades-normativa-2024.html)

**Puntos clave verificados**

- desde períodos iniciados a partir de **01/01/2025**, la reducción general pasa al **20%** del incremento de fondos propios;
- puede subir al **23%**, **26,5%** o **30%** según incremento de plantilla;
- el incremento de plantilla debe mantenerse **3 años**;
- no es correcto seguir modelándola como `10%` con mantenimiento `5 años`.

## 6.3 Tipo de gravamen del Impuesto sobre Sociedades

**Fuente oficial**

- [AEAT - novedades tipo de gravamen desde 2025](https://sede.agenciatributaria.gob.es/Sede/eu_es/impuesto-sobre-sociedades/novedades-impuesto-sobre-sociedades/novedades-normativa-2024/principales-novedades-tributarias-introducidas-ley-diciembre.html)

**Lectura correcta**

- no todas las pymes “pagan el 23%”;
- para 2025:
  - entidades con INCN inferior a **1 millón EUR**: **21%** sobre los primeros **50.000 EUR** de base y **22%** sobre el resto;
  - entidades de reducida dimensión del artículo 101 LIS: **24%**, salvo que les corresponda un tipo diferente;
  - tipo general: **25%**.

Esto hay que cruzarlo con la cifra de negocios real y con si Tank es o no entidad patrimonial.

